# ------------------------------------------------------------
#   Алгоритм Едмондса-Карпа для пошуку максимального потоку
#   у логістичній мережі «Термінал → Склад → Магазин»
# ------------------------------------------------------------

# Створюємо граф
G = nx.DiGraph()

# Додаємо всі ребра до графа
G.add_weighted_edges_from(edges)

# Додаємо джерело (Source) і стік (Sink)
terminals = ["Термінал_1", "Термінал_2"]
warehouses = ["Склад_1", "Склад_2", "Склад_3", "Склад_4"]
shops = [f"Магазин_{i}" for i in range(1, 15)]

# Джерело з'єднується з терміналами (великою пропускною здатністю)
for t in terminals:
    G.add_edge("Source", t, weight=999)

# Магазини з'єднуються зі стоком
for s in shops:
    G.add_edge(s, "Sink", weight=999)

# ------------------------------------------------------------
#   Розрахунок максимального потоку алгоритмом Едмондса-Карпа
# ------------------------------------------------------------
flow_value, flow_dict = nx.maximum_flow(G, "Source", "Sink", capacity="weight", flow_func=nx.algorithms.flow.edmonds_karp)

print("=== Максимальний потік у мережі ===")
print(f"Загальний потік: {flow_value}\n")

# ------------------------------------------------------------
#   Формування підсумкової таблиці: Термінал → Магазин
# ------------------------------------------------------------
print("Термінал\tМагазин\t\tФактичний потік (одиниць)")
print("-" * 50)

# Обхід шляху Термінал → Склад → Магазин
for terminal in terminals:
    for warehouse, w_flows in flow_dict[terminal].items():
        if w_flows > 0:  # є потік до складу
            for shop, s_flows in flow_dict[warehouse].items():
                if s_flows > 0:
                    print(f"{terminal}\t{shop}\t\t{s_flows}")

# ------------------------------------------------------------
#   Візуалізація результату (граф із потоками)
# ------------------------------------------------------------
pos = {
    "Source": (0, 2),
    "Термінал_1": (1, 3),
    "Термінал_2": (1, 1),
    "Склад_1": (3, 3.5),
    "Склад_2": (3, 2.5),
    "Склад_3": (3, 1.5),
    "Склад_4": (3, 0.5),
    "Sink": (9, 2)
}

# Додаємо магазини до позицій
for i, shop in enumerate(shops):
    pos[shop] = (5 + (i % 3), 4 - (i // 3))

plt.figure(figsize=(15, 8))
nx.draw(
    G, pos, with_labels=True,
    node_size=2000, node_color="skyblue",
    font_size=9, font_weight="bold", arrows=True
)

labels = nx.get_edge_attributes(G, 'weight')
filtered_labels = {
    (u, v): w for (u, v), w in labels.items() if u not in ["Source"] and v not in ["Sink"]
}

nx.draw_networkx_edge_labels(G, pos, edge_labels=filtered_labels)
plt.show()
